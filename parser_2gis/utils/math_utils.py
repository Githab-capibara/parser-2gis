"""Модуль математических утилит.

Содержит функции для математических операций и вычислений.
"""

from __future__ import annotations

# =============================================================================
# МАТЕМАТИЧЕСКИЕ ОПЕРАЦИИ
# =============================================================================


def floor_to_hundreds(arg: int | float) -> int:
    """Округляет число вниз до ближайшей сотни.

    Args:
        arg: Число для округления.

    Returns:
        Округлённое вниз число до ближайшей сотни.

    Пример:
        >>> floor_to_hundreds(150)
        100
        >>> floor_to_hundreds(250)
        200
        >>> floor_to_hundreds(50)
        0

    """
    return int((arg // 100) * 100)


# =============================================================================
# ЭКСПОРТ
# =============================================================================

__all__ = ["floor_to_hundreds"]
