"""Константы для модуля Chrome.

Этот модуль содержит все константы используемые в модуле Chrome
для устранения магических чисел в коде.

Пример использования:
    >>> from parser_2gis.chrome.constants import DEFAULT_MEMORY_LIMIT_MB
    >>> print(DEFAULT_MEMORY_LIMIT_MB)
    2048
"""

from __future__ import annotations

import logging
import os

from parser_2gis.shared_config_constants import (  # noqa: F401
    DEFAULT_TTL_HOURS,
    MAX_RESPONSE_SIZE,
)

# Logger для логирования fallback значений
_chrome_constants_logger = logging.getLogger("parser_2gis.chrome.constants")

# =============================================================================
# КОНСТАНТЫ КОНФИГУРАЦИИ CHROME (ОБОСНОВАНИЕ ЗНАЧЕНИЙ)
# =============================================================================

# Лимит оперативной памяти браузера по умолчанию в мегабайтах
# Переопределяется через ENV переменную PARSER_MEMORY_LIMIT_MB
_memory_limit_str = os.environ.get("PARSER_MEMORY_LIMIT_MB")
if _memory_limit_str is not None:
    try:
        DEFAULT_MEMORY_LIMIT_MB: int = int(_memory_limit_str)
    except ValueError:
        DEFAULT_MEMORY_LIMIT_MB = 2048
        _chrome_constants_logger.warning(
            "Некорректное значение PARSER_MEMORY_LIMIT_MB=%s. Используется fallback: %d MB",
            _memory_limit_str,
            DEFAULT_MEMORY_LIMIT_MB,
        )
else:
    DEFAULT_MEMORY_LIMIT_MB = 2048

# Задержка запуска Chrome по умолчанию в секундах
# ОБОСНОВАНИЕ: 0.1 секунды выбрано исходя из:
# - Минимальное время инициализации Chrome DevTools Protocol: ~0.05 сек
# - 0.1 сек обеспечивает запас для стабильного подключения
# - Увеличено для стабильности при параллельном запуске браузеров
DEFAULT_STARTUP_DELAY_SEC: float = 0.1

# Алиас для обратной совместимости (L4: магические числа вынесены в константы)
CHROME_STARTUP_DELAY: float = DEFAULT_STARTUP_DELAY_SEC

# Диапазон портов для remote debugging по умолчанию
# ОБОСНОВАНИЕ: 9222-9322 (100 портов) выбрано исходя из:
# - Порт 9222 - стандартный порт Chrome DevTools
# - 100 портов обеспечивают запас для параллельного запуска множества браузеров
# - Достаточно для поддержки до 100 одновременных сессий парсинга
# ISSUE-#135: Можно настроить через ENV переменные
_DEFAULT_PORT_START = int(os.environ.get("PARSER_DEBUG_PORT_START", "9222"))
_DEFAULT_PORT_END = int(os.environ.get("PARSER_DEBUG_PORT_END", "9322"))
DEFAULT_REMOTE_DEBUGGING_PORT_RANGE: tuple[int, int] = (_DEFAULT_PORT_START, _DEFAULT_PORT_END)

# Максимальная длина JavaScript кода для предотвращения DoS атак
# ОБОСНОВАНИЕ: 5MB выбрано исходя из:
# - Типичный размер JS кода для парсинга: 10KB-500KB
# - Максимальный размер для сложных скриптов: ~1MB
# - 5MB обеспечивает 10x запас для самых сложных случаев
# - Защита от случайной или злонамеренной отправки огромных скриптов
MAX_JS_CODE_LENGTH: int = 100_000  # 100KB - разумный лимит для JavaScript кода

# Максимальный общий размер всех JS скриптов для предотвращения DoS атак
# ОБОСНОВАНИЕ: 50MB выбрано исходя из:
# - Типичное количество скриптов: 10-50
# - Средний размер скрипта: 10KB-100KB
# - 50MB обеспечивает запас для сложных сценариев
# - Защита от атак через отправку множества скриптов
MAX_TOTAL_JS_SIZE: int = 50_000_000  # 50 MB

# Rate limiting для Chrome DevTools Protocol
# ОБОСНОВАНИЕ: 150 вызовов в секунду выбрано исходя из:

# Флаг --no-sandbox для запуска Chrome
# ВНИМАНИЕ: Этот флаг снижает безопасность, но необходим для работы в некоторых
# окружениях (Docker, CI/CD, контейнеры без namespaces). Не использовать в
# окружениях с недоверенным кодом.
CHROME_NO_SANDBOX_FLAG: str = "--no-sandbox"

# Шаблон для --remote-allow-origins с точным указанием порта (без wildcard)
CHROME_REMOTE_ALLOW_ORIGINS_TEMPLATE: str = "http://127.0.0.1:{port}"

# Rate limiting для Chrome DevTools Protocol
# ОБОСНОВАНИЕ: 150 вызовов в секунду выбрано исходя из:
# - CDP может обрабатывать до 150 запросов в секунду без проблем
# - 150 запросов/сек — максимально агрессивный лимит для ускоренного парсинга
# - Достаточно для интенсивной работы без перегрузки браузера
RATE_LIMIT_CALLS: int = 150
RATE_LIMIT_PERIOD: int = 1  # 1 секунда

# Таймауты и задержки
# ОБОСНОВАНИЕ: значения выбраны исходя из максимально агрессивных сценариев парсинга
DEFAULT_EXECUTE_SCRIPT_TIMEOUT_SEC: int = 20  # таймаут выполнения скрипта (увеличено)
DEFAULT_CONNECT_TIMEOUT_SEC: int = 240  # таймаут подключения к Chrome (увеличено)
DEFAULT_PORT_CHECK_TIMEOUT_SEC: float = 0.4  # таймаут проверки порта (увеличено)
DEFAULT_PORT_CHECK_RETRIES: int = 2  # количество повторных проверок порта

# Размеры кэшей
# ОБОСНОВАНИЕ: размеры уменьшены для экономии памяти без потери производительности
PORT_CACHE_MAXSIZE: int = 64  # размер кэша проверки портов

# =============================================================================
# КОНСТАНТЫ БЕЗОПАСНОСТИ
# =============================================================================

# Максимальный размер ответа от внешних сервисов для предотвращения DoS атак
# Определяется в shared_config_constants.py
# MAX_RESPONSE_SIZE импортирован оттуда

# Доля памяти для V8 (75% от общей физической памяти)
# ОБОСНОВАНИЕ: 0.75 выбрано исходя из:
# - Типичное потребление памяти браузером: 50-70% от доступной
# - 75% обеспечивает запас для сложных страниц
# - Предотвращает OOM ошибки при парсинге
MEMORY_FRACTION_FOR_V8: float = 0.75

# Rate limiting для внешних HTTP запросов к 2GIS
# ОБОСНОВАНИЕ: 100 запросов в секунду выбрано исходя из:
# - Максимально агрессивный лимит для интенсивного парсинга
# - Достаточно для быстрого выполнения JavaScript в браузере
# - Не перегружает браузер при параллельных операциях
EXTERNAL_RATE_LIMIT_CALLS: int = 100
EXTERNAL_RATE_LIMIT_PERIOD: int = 1  # 1 секунда

# =============================================================================
# ДОПОЛНИТЕЛЬНЫЕ КОНСТАНТЫ
# =============================================================================

# Стандартный порт для remote debugging
DEFAULT_REMOTE_DEBUGGING_PORT: int = 9222

# Минимальный номер порта (зарезервированные порты 0-1023)
MIN_PORT: int = 1024

# Максимальный номер порта
MAX_PORT: int = 65535

# Количество секунд в часе
SECONDS_PER_HOUR: int = 3600

# CSS цвета для TUI
CSS_COLOR_DARK: str = "#333"
