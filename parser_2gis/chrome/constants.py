# Таймаут для сетевых запросов по умолчанию (в секундах)
# ОБОСНОВАНИЕ: 60 секунд выбрано исходя из:
# - Типичное время ответа внешних API: 1-5 секунд
# - 60 секунд обеспечивают запас для медленных соединений
# - Достаточно для загрузки больших ресурсов
# - Баланс между стабильностью и временем ожидания
DEFAULT_NETWORK_TIMEOUT: int = 60

# Права доступа к временным файлам и директориям
# ОБОСНОВАНИЕ: 0o700 (rwx------) выбрано исходя из:
# - Только владелец может читать/писать/выполнять
# - Предотвращает доступ других пользователей к временным данным
# - Соответствует требованиям безопасности для временных профилей
DEFAULT_FILE_PERMISSIONS: int = 0o700

# TTL кэша по умолчанию в часах
# Определяется в shared_config_constants.py и импортирован оттуда

# Таймаут подключения по умолчанию (в секундах)
# ОБОСНОВАНИЕ: 30 секунд выбрано исходя из:
# - Типичное время подключения к внешним сервисам: 1-5 секунд
# - 30 секунд обеспечивают запас для медленных соединений
# - Баланс между стабильностью и временем ожидания
DEFAULT_CONNECTION_TIMEOUT_SEC: int = 30

# URL для localhost connections
# ISSUE-066: Вынесено в константу для устранения хардкода
LOCALHOST_BASE_URL: str = "http://127.0.0.1:{port}"
