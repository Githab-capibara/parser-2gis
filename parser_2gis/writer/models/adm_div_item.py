"""Модель административно-территориальной единицы.

Предоставляет классы для представления административных единиц:
- Flags - флаги административной единицы
- AdmDivItem - объект административной единицы (город, район и т.д.)
"""

from __future__ import annotations

from pydantic import BaseModel


class Flags(BaseModel):
    """Флаги административной единицы.

    Атрибуты:
        is_default: Заполняется только для type=city и принимает значение true, если город
                    является главным городом текущего проекта (например Новосибирск).
        is_district_area_center: Заполняется только для type=adm_div, subtype=city|settlement
                                 и принимает значение true, если населённый пункт является районным центром.
        is_region_center: Заполняется только для type=adm_div, subtype=city|settlement
                          и принимает значение true, если населённый пункт является областным центром.
        temporary_closed: Строка, наличие которой говорит о том, что филиал временно не работает.
                        В строке выгружается код причины закрытия.
    """

    # Заполняется только для type=city и принимает единственное значение true в случае,
    # если город является главным городом текущего проекта (например Новосибирск)
    is_default: bool | None = None

    # Заполняется только для type=adm_div, subtype=city|settlement
    # и принимает единственное значение true в случае, если населённый пункт является районным центром.
    is_district_area_center: bool | None = None

    # Заполняется только для type=adm_div, subtype=city|settlement
    # и принимает единственное значение true в случае, если населённый пункт является областным центром.
    is_region_center: bool | None = None

    # Cтрока, наличие которой говорит о том, что филиал временно не работает.
    # В строке выгружается код причины закрытия.
    temporary_closed: str | None = None


class AdmDivItem(BaseModel):
    """Модель административно-территориальной единицы.

    Атрибуты:
        id: Идентификатор объекта административной единицы.
        name: Имя объекта.
        caption: Название территории (для использования в функционале «поделиться», для конечных точек маршрута и т.д.).
        type: Тип объекта административной единицы. Возможные значения: `city`, `settlement`, `division`,
               `district`, `living_area`, `place`, `district_area`, `region`, `country`.
        city_alias: Алиас города, в котором находится объект.
        flags: Дополнительные флаги.
        is_default: Заполняется только для type=city и принимает значение true, если город
                    является главным городом текущего проекта (например "Новосибирск").
        detailed_subtype: Детализированный тип административно-территориальной единицы.
    """

    # Идентификатор объекта административной единицы
    id: str | None = None

    # Имя объекта
    name: str

    # Название территории (для использования в функционале «поделиться»,
    # для конечных точек маршрута и т.д.).
    caption: str | None = None

    # Тип объекта административной единицы.
    # Возможные значения:
    # * `city` — город
    # * `settlement` — населённый пункт
    # * `division` — округ
    # * `district` — район
    # * `living_area` — жилмассив, микрорайон
    # * `place` — место
    # * `district_area` — район области
    # * `region` — регион (область/край/республика и т.п.)
    # * `country` - страна
    type: str

    # Алиас города, в котором находится объект
    city_alias: str | None = None

    # Дополнительные флаги
    flags: Flags | None = None

    # Заполняется только для type=city и принимает единственное значение
    # true в случае, если город является главным городом текущего проекта (например "Новосибирск")
    is_default: bool | None = None

    # Детализированный тип административно-территориальной единицы.
    # Возможные значения:
    # * `city` — город
    # * `microdistrict` — микрорайон
    # * `residential_district` — жилмассив
    # * `residential_quarter` — квартал
    # * `poselok` — посёлок
    # * `residential_complex` — жилой комплекс
    # * `selo` — село
    # * `derevnja` — деревня
    # * `cottage_estate` — коттеджный посёлок
    # * `urban_settlement` — посёлок городского типа
    # * `workers_settlement` — рабочий посёлок
    # * `dacha_settlement` — дачный посёлок
    # * `resort_settlement` — курортный посёлок
    # * `stanitsa` — станица
    # * `sloboda` — слобода
    # * `khutor` — хутор
    # * `aul` — аул
    # * `aal` — аал
    # * `town` — (военный) городок
    # * `farmstead` — заимка
    # * `vyselok` — выселок
    # * `municipality` — муниципальное образование
    # * `station` — станция
    # * `townhouse_settlement` — посёлок таунхаусов
    # * `territory` — территория
    # * `cooperative` — кооператив
    # * `partnership` — товарищество
    detailed_subtype: str | None = None
